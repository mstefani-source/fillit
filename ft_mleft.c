int		ft_move(int d)
{
	return (((d | 34952) ^ 34952) << 1);
}

int		ft_mleft(int d, int offcet)
{
	int i = 0;
	
	while (i < offcet)
	{
		d = ft_move(d);
		i++;
	}
	return (d);
}
